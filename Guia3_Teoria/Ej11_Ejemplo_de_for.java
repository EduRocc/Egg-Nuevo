public class Ej11_Ejemplo_de_for {

    public static void main(String[] args) {
        System.out.println("For incrementandose");
        for (int i = 0; i < 10; i++) {
            System.out.println("Imprimo el valor de i: " + i);
        }
        System.out.println("======================");

        System.out.println("For decreciendo");
        for (int i = 10; i > 0; i--) {
            System.out.println("Imprimo el valor de i: " + i);
        }
    }
}
